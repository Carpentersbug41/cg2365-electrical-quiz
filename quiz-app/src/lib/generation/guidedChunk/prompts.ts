export function buildGuidedChunkFramePrompt(input: GuidedChunkGenerationInput, plan: unknown): string {
  const curriculumProfile = getGuidedChunkCurriculumProfile(input.curriculum);
  return [
    'You are generating a complete guided-chunk lesson frame for a tutor-style runtime.',
    'Output JSON only.',
    'Stay strictly inside the source text and plan.',
    'Each teachingCore must be 80-150 words and teach one main idea.',
    'Each chunk must include exactly 3 initial recall questions.',
    'Each chunk may include 1 deeper question.',
    'If you do not need an image or microbreak, set those fields to null.',
    'Do not create assessment items for untaught content.',
    'Do not include generator meta-talk inside teachingCore.',
    'Never write phrases such as "Focus on this idea", "This matters because", or "Keep in mind before answering".',
    'Initial recall questions must be direct factual or process questions, not meta questions about the lesson itself.',
    'MCQs must use plausible distractors from the same topic. Never use placeholder options such as "Irrelevant option" or "Another distractor".',
    'Repair templates must address the named misconception specifically.',
    curriculumProfile.audienceInstruction,
    curriculumProfile.toneInstruction,
    curriculumProfile.chunkInstruction,
    curriculumProfile.questionInstruction,
    input.lessonProfileNotes ?? '',
    '',
    `Lesson code: ${input.lessonCode}`,
    `Title: ${input.title}`,
    `Unit: ${input.unit}`,
    `Topic: ${input.topic}`,
    '',
    'Return a valid JSON object matching the GuidedChunkFrame shape used by the app.',
    'Use runtimeVersion "guided_chunk_v1" and a simple variantId ending in "-generated".',
    'Set metadata.author to "guided_chunk_generator".',
    'For any microbreakAfter value, use null for now unless the plan explicitly requires one.',
    '',
    'Plan JSON:',
    JSON.stringify(plan, null, 2),
    '',
    'Source text:',
    input.sourceText,
  ].join('\n');
}

