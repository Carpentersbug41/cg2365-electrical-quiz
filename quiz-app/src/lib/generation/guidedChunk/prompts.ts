export function buildGuidedChunkScorePrompt(
  input: Pick<GuidedChunkGenerationInput, 'curriculum' | 'lessonProfileNotes' | 'sourceText'>,
  frame: unknown
): string {
  const curriculumProfile = getGuidedChunkCurriculumProfile(input.curriculum);
  return [
    'You are scoring a guided-chunk lesson frame for a tutor-style runtime.',
    'Return JSON only.',
    'Score it using the same pedagogical priorities as the main V1 lesson pipeline.',
    'Judge learner-facing teaching quality, not just schema completeness or technical correctness.',
    'Use this rubric exactly:',
    '- beginnerClarity (0-30): plain language, concrete explanation, direct definition, misconception handling, readable first-turn teaching',
    '- teachingBeforeTesting (0-25): the answer is taught before any question, progression is teach then recall then deeper check, no cold questions',
    '- markingRobustness (0-20): expectedConcepts are specific and gradeable, question scope matches answer scope, repair is usable',
    '- alignmentToLO (0-15): content and checks align to the intended LO or chunk goal and do not drift into untaught material',
    '- questionQuality (0-10): concrete, useful, non-generic, non-meta, non-placeholder questions with sensible progression',
    'Penalize hard for generic openings, title restatement, placeholder setup text, note-like prose, overloaded first turns, and abstract questions such as "what is the key fact in this chunk".',
    'Scores must use integer values and the total must be between 0 and 100.',
    'Keep issues concise and action-oriented.',
    curriculumProfile.audienceInstruction,
    curriculumProfile.chunkInstruction,
    curriculumProfile.questionInstruction,
    input.lessonProfileNotes ?? '',
    '',
    'Return JSON with this shape:',
    '{',
    '  "breakdown": {',
    '    "beginnerClarity": 0,',
    '    "teachingBeforeTesting": 0,',
    '    "markingRobustness": 0,',
    '    "alignmentToLO": 0,',
    '    "questionQuality": 0',
    '  },',
    '  "summary": "string",',
    '  "refinementNotes": ["string"],',
    '  "issues": [',
    '    {',
    '      "category": "beginnerClarity|teachingBeforeTesting|markingRobustness|alignmentToLO|questionQuality",',
      '      "problem": "string",',
      '      "suggestion": "string"',
    '    }',
    '  ]',
    '}',
    '',
    'Frame JSON:',
    JSON.stringify(frame, null, 2),
    '',
    'Source text:',
    input.sourceText,
  ].join('\n');
}

export function buildGuidedChunkRefinePrompt(
  input: GuidedChunkGenerationInput,
  frame: unknown,
  score: { summary: string; issues: Array<{ category: string; problem: string; suggestion: string }> }
): string {
  const curriculumProfile = getGuidedChunkCurriculumProfile(input.curriculum);
  return [
    'You are refining a guided-chunk lesson frame after scoring.',
    'Return JSON only.',
    'Rewrite the frame to improve learner-facing quality without changing the curriculum scope.',
    'Fix every issue listed below.',
    'Do not include generator meta-talk in teachingCore.',
    'Do not let the teaching core read like standards notes, glossary notes, or etymology commentary.',
    'Use direct factual recall questions, not meta questions about the lesson.',
    'Use natural deeper questions.',
    'Repair templates must sound like a tutor, not an internal validator.',
    'Short-answer expectedConcepts must contain concise answer points, not copies of the prompt.',
    'MCQs must use plausible topic-relevant distractors.',
    curriculumProfile.audienceInstruction,
    curriculumProfile.toneInstruction,
    curriculumProfile.chunkInstruction,
    curriculumProfile.questionInstruction,
    input.lessonProfileNotes ?? '',
    '',
    'Score summary:',
    score.summary,
    '',
    'Issues to fix:',
    ...score.issues.map((issue, index) => `${index + 1}. [${issue.category}] ${issue.problem} Fix: ${issue.suggestion}`),
    '',
    'Current frame JSON:',
    JSON.stringify(frame, null, 2),
    '',
    'Source text:',
    input.sourceText,
  ].join('\n');
}
