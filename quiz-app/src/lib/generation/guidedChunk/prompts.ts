export function buildGuidedChunkChunkPrompt(
  input: GuidedChunkGenerationInput,
  scaffold: GuidedChunkLoScaffold,
  chunk: GuidedChunkScaffoldChunk,
  memory: GuidedChunkGenerationMemory,
  priorChunkTexts: string[]
): string {
  const curriculumProfile = getGuidedChunkCurriculumProfile(input.curriculum);
  const cg2365PromptRules =
    input.curriculum === 'cg2365'
      ? [
          'For C&G 2365, open by naming the electrical thing and what it does or how it differs in practice.',
          'Bad opening example: "This step links the earthing arrangement to the ADS parts..."',
          'Bad opening example: "You need to identify the main earthing system types..."',
          'Good opening example: "The first difference between TT, TN-S, and TN-C-S is how the installation gets its earth path."',
          'Good opening example: "In TT the installation relies on its own earth electrode, while in TN systems the supplier provides the earth path."',
          'Do not restate the title or learning goal inside the teachingCore.',
        ]
      : [];
  return [
    'You are generating one chunk for a guided-chunk tutor lesson.',
    'Output JSON only.',
    'Generate only the current chunk.',
    'Do not restate earlier chunks except for one brief linking phrase if needed.',
    'Do not include meta teaching language.',
    'Teaching core must be 80-150 words and cover one main idea only.',
    'The teaching core must read like a live tutor explanation, not a textbook note or mnemonic dump.',
    'Include exactly 3 initial recall questions and 1 deeper question.',
    'Recall questions must test the current chunk, not prior chunks.',
    'Repair templates must match the current chunk misconceptions.',
    curriculumProfile.audienceInstruction,
    curriculumProfile.toneInstruction,
    curriculumProfile.chunkInstruction,
    curriculumProfile.questionInstruction,
    ...cg2365PromptRules,
    input.lessonProfileNotes ?? '',
    '',
    `Lesson code: ${input.lessonCode}`,
    `LO: ${scaffold.loTitle}`,
    `Current chunk: ${chunk.chunkId}`,
    `Current goal: ${chunk.learningGoal}`,
    '',
    'Already covered concepts:',
    JSON.stringify(memory.coveredConcepts, null, 2),
    '',
    'Already introduced vocab:',
    JSON.stringify(memory.introducedVocab, null, 2),
    '',
    'Already handled misconceptions:',
    JSON.stringify(memory.handledMisconceptions, null, 2),
    '',
    'Prior chunk texts in this LO:',
    JSON.stringify(priorChunkTexts, null, 2),
    '',
    'Current chunk blueprint:',
    JSON.stringify(chunk, null, 2),
    '',
    'Return JSON with this shape:',
    '{',
    '  "chunkId": "string",',
    '  "learningGoal": "string",',
    '  "teachingCore": "string",',
    '  "vocabPack": [{ "term": "string", "definition": "string" }],',
    '  "initialRecallQuestions": [',
    '    { "id": "string", "prompt": "string", "expectedConcepts": ["string"] }',
    '  ],',
    '  "candidateFollowUpModes": ["probe", "repair"],',
    '  "candidateDeeperQuestion": { "id": "string", "prompt": "string", "expectedConcepts": ["string"] },',
    '  "misconceptionCodes": ["string"],',
    '  "repairTemplates": [',
    '    {',
    '      "misconceptionCode": "string",',
    '      "shortCorrection": "string",',
    '      "contrastPrompt": "string",',
    '      "retryPrompt": "string"',
    '    }',
    '  ],',
    '  "assetInjection": null,',
    '  "microbreakAfter": null',
    '}',
    '',
    'Source text:',
    input.sourceText,
  ].join('\n');
}

